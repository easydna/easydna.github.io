function _categoria(parent){

    return Array.from(new Set(situacao.filter(situacao => situacao.humor == parent.humor).map(situacao => situacao.categoria)))
}

function _item(parent){

    return Array.from(new Set(situacao.filter(situacao => situacao.humor == parent.humor && situacao.categoria == parent.categoria ).map(situacao => situacao.item)))
}

function _situacao(parent){

    return Array.from(new Set(situacao.filter(situacao => situacao.humor == parent.humor && situacao.categoria == parent.categoria && situacao.item == parent.item).map(situacao => situacao.situacao)))
}